// Helper function to check if a value exists in MLS
export function findInMLS(searchTerm: string): {
  found: boolean;
  field?: 'zone' | 'area' | 'community' | 'subdivision';
  exactMatch?: string;
  suggestions?: string[];
} {
  const search = searchTerm.toLowerCase().trim();

  // NEW — check cities first
  const cityMatch = MLS_REFERENCE.zones.find(c => c.toLowerCase() === search);
  if (cityMatch) {
    return { found: true, field: 'city', exactMatch: cityMatch };
  }

  // Check zones
  const zoneMatch = MLS_REFERENCE.zones.find(z => z.toLowerCase() === search);
  if (zoneMatch) {
    return { found: true, field: 'zone', exactMatch: zoneMatch };
  }

  // Check areas
  const areaMatch = MLS_REFERENCE.areas.find(a => a.toLowerCase() === search);
  if (areaMatch) {
    return { found: true, field: 'area', exactMatch: areaMatch };
  }

  // Check communities
  const communityMatch = MLS_REFERENCE.communities.find(c => c.toLowerCase() === search);
  if (communityMatch) {
    return { found: true, field: 'community', exactMatch: communityMatch };
  }

  // Check subdivisions
  const subdivisionMatch = MLS_REFERENCE.subdivisions.find(s => s.toLowerCase() === search);
  if (subdivisionMatch) {
    return { found: true, field: 'subdivision', exactMatch: subdivisionMatch };
  }

  // Not found - provide suggestions
  const suggestions: string[] = [];
  
  [...MLS_REFERENCE.zones, ...MLS_REFERENCE.areas, ...MLS_REFERENCE.communities, ...MLS_REFERENCE.subdivisions]
    .forEach(value => {
      if (value.toLowerCase().includes(search) || search.includes(value.toLowerCase())) {
        suggestions.push(value);
      }
    });

    const suggestions: string[] = [];

  [
    ...MLS_REFERENCE.cities,
    ...MLS_REFERENCE.zones,
    ...MLS_REFERENCE.areas,
    ...MLS_REFERENCE.communities,
    ...MLS_REFERENCE.subdivisions
  ].forEach(val => {
    if (val.toLowerCase().includes(search)) {
      suggestions.push(val);
    }
  });

  return { found: false, suggestions: suggestions.slice(0, 5) };
}
